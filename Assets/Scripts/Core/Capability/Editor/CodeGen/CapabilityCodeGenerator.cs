        [MenuItem(MenuGeneratePath, false, 20)]
        public static void GenerateAllCapability()
        {
            if (EditorApplication.isPlaying)
            {
                Debug.LogWarning("[CapabilityCodeGenerator] Play Mode 下不能执行代码生成。");
                return;
            }

            // 优先调用独立 Python 脚本，不依赖 Unity 编译状态。
            if (TryRunPythonScript("Tools/generate_all_capability.py"))
            {
                TryPatchGeneratedRegister();
                return;
            }

            try
            {
                List<CapabilityMetadata> metadata = CollectCapabilities();
                metadata.Sort(CompareCapabilityMetadata);

                string template = LoadTemplate();
                string orderLines = BuildOrderLines(metadata);
                string registerLines = BuildRegisterLines(metadata);
                string content =
                    ApplyTemplate(template, orderLines, metadata.Count, registerLines);

                string outputFullPath = Path.GetFullPath(OutputAssetPath);
                string outputDirectory = Path.GetDirectoryName(outputFullPath);
                if (!string.IsNullOrWhiteSpace(outputDirectory) &&
                    !Directory.Exists(outputDirectory))
                {
                    if (outputDirectory != null)
                    {
                        Directory.CreateDirectory(outputDirectory);
                    }
                }

                File.WriteAllText(outputFullPath, content, Encoding.UTF8);
                AssetDatabase.Refresh();

                Debug.Log(
                    $"[CapabilityCodeGenerator] 生成完成: {OutputAssetPath} (共 {metadata.Count} 个 Capability)。");
            }
            catch (Exception exception)
            {
                Debug.LogError($"[CapabilityCodeGenerator] 生成失败: {exception}");
            }
        }

